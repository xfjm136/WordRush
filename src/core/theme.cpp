ThemePalette ThemeByIndex(std::size_t index, bool use_background) {
  auto themes = BuiltinThemes();
  if (themes.empty()) {
    return {};
  }
  index = std::min(index, themes.size() - 1);
  ThemePalette palette = themes[index];
  palette.use_background = use_background;
  return palette;
}

